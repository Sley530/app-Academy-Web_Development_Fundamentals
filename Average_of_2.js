/* In this exercise I will
 * Write a method averageOfTwo(num1, num2)
 * that returns the average of two numbers.
 *
 * @author Sley
 */

function averageOfTwo(num1, num2) {
    let avg = (num1 + num2) / 2;
    return avg;
  }
  
  console.log(averageOfTwo(3, 7)); // => 5.0
  console.log(averageOfTwo(16, 5)); // => 10.5
  console.log(averageOfTwo(2, 7.5)); // => 4.75